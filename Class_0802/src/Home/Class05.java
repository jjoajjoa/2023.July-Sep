package Home;

public class Class05 {

	public static void main(String[] args) {
		
		// 8*8 배열 만들기
		int[][] arr = new int[8][8];
		
		// 8*8 1부터 차례대로 넣기

		
		
		// 8*8 1부터 차례대로 넣기
		for (int i=0; i<8; i++) {
			for (int j=0; j<8; j++) {				
				arr[i][j] = j+1;
				System.out.print(arr[i][j]);
			}
		System.out.println();
		}

		
		
	}

}


